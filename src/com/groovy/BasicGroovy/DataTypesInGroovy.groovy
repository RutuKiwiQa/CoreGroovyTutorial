package com.groovy.BasicGroovy
/**
 * Data types in groovy
 * byte
 * short
 * int
 * long
 * float
 * double
 * char
 * Boolean
 * String
 */

//1. Byte
byte b = 10
println "Byte : " +b
println "Minimum Range Value : " +Byte.MIN_VALUE
println "Maximum Range Value : " +Byte.MAX_VALUE
println "========================================"

//2. Short
short s = 100
println "Short : " +s
println "Minimum Range Value : " + Short.MIN_VALUE
println "Maximum Range Value : " + Short.MAX_VALUE
println "========================================"

//3. Integer
int i = 20
println "Integer : " +i
println "Minimum Range Value : " + Integer.MIN_VALUE
println "Maximum Range Value : " + Integer.MAX_VALUE
println "=========================================="

//4. Long
long l = 9999090090;
println "Long : " +l
println "Minimum Range Value : " + Long.MIN_VALUE
println "Maximum Range Value : " + Long.MAX_VALUE
println "=========================================="

//5. Float
float f = 35452.5;
println "Float : " +f
println "Minimum Range Value : " + Float.MIN_VALUE
println "Maximum Range Value : " + Float.MAX_VALUE
println "=========================================="

//6. Double
double d = 444435452.5;
println "Double : " +d
println "Minimum Range Value : " + Double.MIN_VALUE
println "Maximum Range Value : " + Double.MAX_VALUE
println "=========================================="

//7. Char
char c = 'R';
println "Char : " +c
println "=========================================="

//8. Boolean
boolean bool = true;
println "Boolean : " +bool
println "=========================================="


//9. String
String string = "Rutu";
println "String : " +string
println "=========================================="

